<?php

function compute_tax($salary){

    /*
        SEMI-MONTHLY WITHHOLDING TAX
        Effective January 1, 2023

        IMPORTANT:
        Salary passed here should already be
        the SEMI-MONTHLY salary.

        Example:
        Monthly Salary = 30,000
        Semi-Monthly = 15,000

        Pass 15,000 here.
    */

    $tax = 0;

    // =====================================================
    // BRACKET 1
    // ₱10,417 and below
    // =====================================================

    if($salary <= 10417){

        $tax = 0;
    }

    // =====================================================
    // BRACKET 2
    // ₱10,417 - ₱16,666
    // 15% over ₱10,417
    // =====================================================

    elseif($salary <= 16666){

        $tax =
            ($salary - 10417) * 0.15;
    }

    // =====================================================
    // BRACKET 3
    // ₱16,667 - ₱33,332
    // ₱937.50 + 20% excess over ₱16,667
    // =====================================================

    elseif($salary <= 33332){

        $tax =
            937.50 +
            (($salary - 16667) * 0.20);
    }

    // =====================================================
    // BRACKET 4
    // ₱33,333 - ₱83,332
    // ₱4,270.70 + 25% excess over ₱33,333
    // =====================================================

    elseif($salary <= 83332){

        $tax =
            4270.70 +
            (($salary - 33333) * 0.25);
    }

    // =====================================================
    // BRACKET 5
    // ₱83,333 - ₱333,332
    // ₱16,770.70 + 30% excess over ₱83,333
    // =====================================================

    elseif($salary <= 333332){

        $tax =
            16770.70 +
            (($salary - 83333) * 0.30);
    }

    // =====================================================
    // BRACKET 6
    // ₱333,333 and above
    // ₱91,770.70 + 35% excess over ₱333,333
    // =====================================================

    else{

        $tax =
            91770.70 +
            (($salary - 333333) * 0.35);
    }

    return round($tax, 2);
}

?>